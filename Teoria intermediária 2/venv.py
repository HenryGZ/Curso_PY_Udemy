# um ambiente virtual carrega toda a sua
# instalação do python para uma pasta no caminho
# escolhido
# ao ativar um ambiente virtual, a instalação do
# ambiente virtual será usada
# venv é o modulo que vamos usar para criar
# ambientes virtuais
# você pode dar o nome que preferir para um 
# ambiente virtual, mas os mais comuns são:
# venv env .venv .env

# resumindo serve para criar projetos diferentes
# para diferentes funionalidades e não misturar
# bibliotecas diferentes para diversas funionalidades
